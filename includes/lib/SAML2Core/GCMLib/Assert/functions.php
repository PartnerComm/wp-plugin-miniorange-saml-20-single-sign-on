<?php


namespace Assert;

function that($zw, $dx = null, $mi = null)
{
    return Assert::that($zw, $dx, $mi);
}
function thatAll($Uj, $dx = null, $mi = null)
{
    return Assert::thatAll($Uj, $dx, $mi);
}
function thatNullOr($zw, $dx = null, $mi = null)
{
    return Assert::thatNullOr($zw, $dx, $mi);
}
function lazy()
{
    return Assert::lazy();
}
