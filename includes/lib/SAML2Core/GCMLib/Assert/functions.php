<?php


namespace Assert;

function that($DE, $K3 = null, $hG = null)
{
    return Assert::that($DE, $K3, $hG);
}
function thatAll($XV, $K3 = null, $hG = null)
{
    return Assert::thatAll($XV, $K3, $hG);
}
function thatNullOr($DE, $K3 = null, $hG = null)
{
    return Assert::thatNullOr($DE, $K3, $hG);
}
function lazy()
{
    return Assert::lazy();
}
